package Composicao;

public class Guerreiro {
	
	private String nome;
	private String origem;
	private String arma;
	
	
	
	public Guerreiro(String nome, String origem, String arma) {
		this.nome = nome;
		this.origem = origem;
		this.arma = arma;
	}



	public String getNome() {
		return nome;
	}



	public void setNome(String nome) {
		this.nome = nome;
	}



	public String getOrigem() {
		return origem;
	}



	public void setOrigem(String origem) {
		this.origem = origem;
	}



	public String getArma() {
		return arma;
	}



	public void setArma(String arma) {
		this.arma = arma;
	}
	
	
	
	

}
